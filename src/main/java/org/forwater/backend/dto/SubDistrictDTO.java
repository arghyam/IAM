package org.forwater.backend.dto;

public class SubDistrictDTO {

    private String subDistricts;
    private String osid;
    private String fKeysubDistricts;

    public String getSubDistricts() {
        return subDistricts;
    }

    public void setSubDistricts(String subDistricts) {
        this.subDistricts = subDistricts;
    }

    public String getOsid() {
        return osid;
    }

    public void setOsid(String osid) {
        this.osid = osid;
    }

    public String getfKeysubDistricts() {
        return fKeysubDistricts;
    }

    public void setfKeysubDistricts(String fKeysubDistricts) {
        this.fKeysubDistricts = fKeysubDistricts;
    }
}
